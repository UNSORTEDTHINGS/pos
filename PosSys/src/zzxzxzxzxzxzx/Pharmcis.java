package zzxzxzxzxzxzx;

public class Pharmcis {
	
	String name;
	int id;
	int numberOfTrans;
	double totalMade;
	
	public Pharmcis(String name, int id) {
		this.name = name;
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNumberOfTrans() {
		return numberOfTrans;
	}
	public void setNumberOfTrans(int numberOfTrans) {
		this.numberOfTrans = numberOfTrans;
	}
	public double getTotalMade() {
		return totalMade;
	}
	public void setTotalMade(double totalMade) {
		this.totalMade = totalMade;
	}
	
	
	

}
